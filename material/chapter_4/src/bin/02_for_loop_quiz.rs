/// 메인 함수 - 프로그램의 진입점
fn main() {
    // 1. 기본 for 루프 (마지막 값 제외)
    // 1부터 4까지 출력 (5 제외)
    println!("=== 기본 for 루프 ===");
    for i in 1..5 /* TODO: 1..5 범위 */ {
        print!("{},", i);
    }
    println!();  // 기대 출력: 1,2,3,4,

    // 2. 마지막 값 포함 (a..=b)
    // 1부터 5까지 모두 출력 (5 포함)
    println!("\n=== 마지막 값 포함 ===");
    for i in 1..=5 /* TODO: 1부터 5까지 포함하는 범위 */ {
        print!("{},", i);
    }
    println!();  // 기대 출력: 1,2,3,4,5,

    // 3. 피라미드 출력
    // 1줄에 *, 2줄에 **, 3줄에 *** 출력
    println!("\n=== 피라미드 ===");
    for i in 1..=3 {
        for _ in 1..=i /* TODO: 1부터 i까지 범위 */ {
            print!("*");
        }
        println!();
    }
}
