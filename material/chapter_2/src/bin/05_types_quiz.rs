//! 연습문제 5: 타입과 타입 캐스팅
//!
//! 아래 코드의 빈칸(/* TODO */)을 채워서 프로그램을 완성하세요.
//! 실행 방법: cargo run --bin 05_types_quiz

fn main() {
    // =====================
    // 문제 1: 정수 타입 선언
    // 8비트 부호 있는 정수 small을 -100으로 선언하세요.
    // =====================
    /* TODO: i8 타입 변수를 선언하세요 */
    let small: i8 = -100;
    println!("i8 값: {}", small);

    // =====================
    // 문제 2: 부호 없는 정수
    // 32비트 부호 없는 정수 count를 1000으로 선언하세요.
    // =====================
    /* TODO: u32 타입 변수를 선언하세요 */
    let count: u32 = 1000;
    println!("u32 값: {}", count);

    // =====================
    // 문제 3: 실수 타입
    // 64비트 부동소수점 변수 price를 99.99로 선언하세요.
    // =====================
    /* TODO: f64 타입 변수를 선언하세요 */
    let price: f64 = 99.99;
    println!("가격: {}", price);

    // =====================
    // 문제 4: 실수 -> 정수 캐스팅
    // float_val을 정수로 캐스팅하세요.
    // 기대 출력: "3.7 -> 3"
    // =====================
    let float_val: f64 = 3.7;
    /* TODO: as 키워드로 i32로 캐스팅하세요 */
    let int_val = float_val as i32;
    println!("{} -> {}", float_val, int_val);

    // =====================
    // 문제 5: 정수 -> 실수 캐스팅
    // integer를 실수로 캐스팅하세요.
    // 기대 출력: "42 -> 42.0" (또는 42)
    // =====================
    let integer: i32 = 42;
    /* TODO: as 키워드로 f64로 캐스팅하세요 */
    let float_result = integer as f64;
    println!("{} -> {}", integer, float_result);

    // =====================
    // 문제 6: 다양한 타입 연산
    // a(i32)와 b(f64)를 더하여 결과를 출력하세요.
    // a를 f64로 캐스팅하여 연산해야 합니다.
    // 기대 출력: "10 + 3.5 = 13.5"
    // =====================
    let a: i32 = 10;
    let b: f64 = 3.5;
    /* TODO: a를 f64로 캐스팅하여 b와 더하세요 */
    let result = a as f64 + b;
    println!("{} + {} = {}", a, b, result);

    // =====================
    // 문제 7: char -> 정수 캐스팅
    // 문자 'A'의 ASCII 값을 출력하세요.
    // 기대 출력: "'A'의 ASCII 값: 65"
    // =====================
    let ch: char = 'A';
    /* TODO: char를 u8로 캐스팅하세요 */
    let ascii = ch as u8;
    println!("'{}'의 ASCII 값: {}", ch, ascii);
}
